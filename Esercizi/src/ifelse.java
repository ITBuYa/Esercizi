
public class ifelse {
	
	public static void main(String[] args) {
		
		int myNumber1 = 5;
		int myNumber2 = 5;
		
		if(myNumber1 == myNumber2) {
			System.out.println("myNumber1 is equal to myNumber2");
		}
		else {
			System.out.println("myNumber1 is not equal to myNumber2");
		}
		
		int myNumber3 = 21;
		int myNumber4 = 20;
		
		if(myNumber3 == myNumber4) {
			System.out.println("myNumber3 is equal to myNumber4");
		}
		else if(myNumber3 < myNumber4) {
			System.out.println("myNumber3 is less than myNumber4");
		}
		else {
			System.out.println("myNumber3 is bigger than myNumber4");
		}		
		}
	}


